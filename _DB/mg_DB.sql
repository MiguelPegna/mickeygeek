CREATE TABLE `favorites`(
    `id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
    `user` BIGINT(20) UNSIGNED NOT NULL,
    `product` BIGINT(20) UNSIGNED NOT NULL,
    PRIMARY KEY (`id`),
    FOREIGN KEY(`user`) REFERENCES `users`(`id`)  ON DELETE CASCADE,
    FOREIGN KEY(`product`) REFERENCES `products`(`id`)  ON DELETE CASCADE
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `photos`(
    `id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
    `product` BIGINT(20) UNSIGNED,
    `picture` VARCHAR(255) NULL DEFAULT '001.jpg',
    `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    FOREIGN KEY(`product`) REFERENCES `products`(`id`) ON DELETE CASCADE
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `addresses`(
    `id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
    `user` BIGINT(20) UNSIGNED,
    `receptor` VARCHAR(255) NOT NULL,
    `cp` INT(11) NOT NULL,
    `estado` VARCHAR(255) NOT NULL,
    `localidad` VARCHAR(255) NOT NULL,
    `colonia` VARCHAR(255) NOT NULL,
    `calle` VARCHAR(255) NOT NULL,
    `numero` VARCHAR(11) DEFAULT 'S/N',
    `numeroInt` VARCHAR(50) DEFAULT 'N/A',
    `calleEx1` VARCHAR(50) DEFAULT '',
    `calleEx2` VARCHAR(50) DEFAULT '',
    `telefono` VARCHAR(255) DEFAULT '',
    `info` VARCHAR(255) DEFAULT '',
    `main` INT(11) DEFAULT 0,
    PRIMARY KEY (`id`),
    FOREIGN KEY(`user`) REFERENCES `users`(`id`) ON DELETE CASCADE
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `orders`(
    `id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
    `user` BIGINT(20) UNSIGNED,
    `address` BIGINT(20) UNSIGNED,
    `operation` BIGINT(20) NOT NULL,
    `ticket` BIGINT(20)NOT NULL,
    `pago` VARCHAR(255),
    `payment` VARCHAR(255),
    `subtotal` FLOAT(20) NOT NULL,
    `envio` FLOAT(20) NOT NULL DEFAULT 89,
    `total` FLOAT(20) NOT NULL,
    `status` INT(2) NOT NULL DEFAULT 1 COMMENT '1-Pendiente, 0-Hecho',
    `order_created_at` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    FOREIGN KEY(`user`) REFERENCES `users`(`id`) ON DELETE CASCADE,
    FOREIGN KEY(`address`) REFERENCES `addresses`(`id`)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `sales`(
    `id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
    `user` BIGINT(20) UNSIGNED,
    `product` BIGINT(20) UNSIGNED,
    `order` BIGINT(20) UNSIGNED,
    `ticket` BIGINT(20) UNSIGNED,
    `quantity` BIGINT(20),
    `size` VARCHAR(255) NOT NULL DEFAULT 'M',
    `gender` VARCHAR(255)NOT NULL,
    `total` FLOAT(20) NOT NULL,
    `sale_created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    FOREIGN KEY(`user`) REFERENCES `users`(`id`),
    FOREIGN KEY(`product`) REFERENCES `products`(`id`),
    FOREIGN KEY(`order`) REFERENCES `orders`(`id`)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

